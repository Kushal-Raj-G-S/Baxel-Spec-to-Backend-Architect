def generate_code_skeleton(spec) -> Dict[str, str]:
    # Models skeleton
    models_code = "# SQLAlchemy models generated by Baxel\n\n"
    models_code += "from sqlalchemy import Column, String, Integer, DateTime, ForeignKey, Boolean\n"
    models_code += "from sqlalchemy.orm import relationship, declarative_base\n\nBase = declarative_base()\n\n"
    
    for table in spec.database.tables:
        models_code += f"class {table.name.title().replace('_', '')}(Base):\n"
        models_code += f"    __tablename__ = '{table.name}'\n"
        models_code += f"    # {table.description}\n\n"
        for col in table.columns:
            col_type = col.type.upper()
            if "UUID" in col_type:
                type_str = "String(36)"
            elif "INT" in col_type:
                type_str = "Integer"
            elif "DATE" in col_type or "TIME" in col_type:
                type_str = "DateTime"
            elif "BOOL" in col_type:
                type_str = "Boolean"
            else:
                type_str = "String(255)"
                
            args = []
            if col.is_primary_key:
                args.append("primary_key=True")
            if not col.is_nullable:
                args.append("nullable=False")
            if col.foreign_key:
                args.append(f"ForeignKey('{col.foreign_key}')")
                
            args_str = ", ".join(args)
            models_code += f"    {col.name} = Column({type_str}{', ' + args_str if args_str else ''})\n"
        models_code += "\n"
        
    # Routers skeleton
    routers_code = "# FastAPI routers generated by Baxel\n\n"
    routers_code += "from fastapi import APIRouter, Depends, HTTPException\n"
    routers_code += "from typing import List\n\nrouter = APIRouter()\n\n"
    
    for ep in spec.endpoints:
        normalized_name = ep.summary.lower().replace(" ", "_").replace("/", "_").replace("-", "_")
        routers_code += f"@router.{ep.method.lower()}('{ep.path}', summary='{ep.summary}')\n"
        routers_code += f"async def {normalized_name}(\n"
        if ep.is_protected:
            routers_code += "    current_user = Depends(get_current_user)\n"
        routers_code += "):\n"
        routers_code += f"    # TODO: Implement endpoint\n"
        routers_code += f"    return {{'message': '{ep.summary}'}}\n\n"
        
    return {
        "models": models_code,
        "routers": routers_code,
        "services": "# Business logic & helpers\n"
    }

def generate_migration_sql(spec) -> str:
    sql = "-- DB migration script generated by Baxel\n\n"
    for table in spec.database.tables:
        sql += f"CREATE TABLE IF NOT EXISTS {table.name} (\n"
        cols = []
        for col in table.columns:
            col_type = col.type.upper()
            if "UUID" in col_type:
                db_type = "UUID"
            elif "INT" in col_type:
                db_type = "INTEGER"
            elif "DATE" in col_type or "TIME" in col_type:
                db_type = "TIMESTAMP"
            elif "BOOL" in col_type:
                db_type = "BOOLEAN"
            else:
                db_type = "VARCHAR(255)"
                
            constraints = []
            if col.is_primary_key:
                constraints.append("PRIMARY KEY")
            if not col.is_nullable:
                constraints.append("NOT NULL")
            if col.foreign_key:
                constraints.append(f"REFERENCES {col.foreign_key.split('.')[0]}(id)")
                
            cols.append(f"    {col.name} {db_type} {' '.join(constraints)}".strip())
        sql += ",\n".join(cols)
        sql += "\n);\n\n"
    return sql

