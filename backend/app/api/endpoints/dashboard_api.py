@pipelines_router.post("/run")
async def run_pipeline_job(
    payload: PipelineRunRequest, 
    response: Response, 
    user=Depends(get_current_user),
    db: Session = Depends(get_db)
):
    uid = user.get("sub")
    logger.info(f"Running synchronous pipeline for user {uid}...")
    
    # 1. NLP Intent routing & extraction
    ir, confidence_score, rules, status = await run_nlp_pipeline(payload.spec_content)
    
    # 2. Agent Swarm debate cycles
    spec = await run_agent_swarm(
        ir=ir,
        rules=rules,
        prompt_used=payload.spec_content,
        parent_spec_id=payload.spec_id,
        confidence_score=confidence_score,
        generation_status=status
    )
    
    # 3. Dynamic schema mappings and code generation
    db_tables = []
    for table in spec.database.tables:
        db_tables.append({
            "name": table.name,
            "fields": [
                {
                    "name": col.name,
                    "type": col.type,
                    "constraints": ["primary_key"] if col.is_primary_key else ([] if col.is_nullable else ["not_null"])
                }
                for col in table.columns
            ]
        })
        
    api_endpoints = []
    for ep in spec.endpoints:
        api_endpoints.append({
            "method": ep.method,
            "path": ep.path,
            "desc": ep.summary,
            "errors": ep.error_responses if ep.error_responses else []
        })
        
    business_rules = []
    for r in spec.business_rules:
        business_rules.append({
            "name": r.rule,
            "type": "business_rule",
            "trigger_condition": r.reason
        })
        
    # Auto-deduct relationships from columns that have foreign keys
    relationships = []
    for table in spec.database.tables:
        for col in table.columns:
            if col.foreign_key:
                target_table = col.foreign_key.split(".")[0]
                relationships.append(f"{table.name}.{col.name} -> {target_table}.id")
                
    # Generate code skeletons & migrations
    code_skeleton = generate_code_skeleton(spec)
    migration_sql = generate_migration_sql(spec)
    
    result = {
        "summary": spec.spice.design_rationale,
        "entities": db_tables,
        "relationships": relationships,
        "join_tables": [],
        "endpoints": api_endpoints,
        "rules": business_rules,
        "code_skeleton": code_skeleton,
        "migration_sql": migration_sql,
        "anti_fragility": spec.anti_fragility.model_dump() if spec.anti_fragility else None,
        "__meta": {
            "source": "nvidia",
            "model": "meta/llama-3.1-70b-instruct",
            "spec_expansion": {
                "original_chars": len(payload.spec_content),
                "expanded_chars": len(payload.spec_content) * 3,
                "inferred_count": len(db_tables) + len(api_endpoints)
            }
        }
    }
    
    # Save the generated spec to DB for chatbot/history query
    try:
        existing_spec = db.query(SpecModel).filter(SpecModel.id == payload.spec_id).first()
        if not existing_spec:
            db_spec = SpecModel(
                id=payload.spec_id,
                parent_spec_id=None,
                version=1,
                prompt_used=payload.spec_content,
                generated_json=spec.model_dump()
            )
            db.add(db_spec)
            db.commit()
            logger.info(f"Successfully saved spec {payload.spec_id} to SQL database.")
        else:
            existing_spec.generated_json = spec.model_dump()
            db.commit()
            logger.info(f"Successfully updated spec {payload.spec_id} in SQL database.")
    except Exception as db_err:
        logger.error(f"Failed to save generated spec to database: {db_err}")

    # 4. Save the run history to in_memory_db
    run_id = str(uuid.uuid4())
    new_run = {
        "id": run_id,
        "project_id": payload.project_id,
        "spec_id": payload.spec_id,
        "user_id": uid,
        "status": "completed",
        "result": result,
        "created_at": datetime.datetime.utcnow().isoformat()
    }
    in_memory_db["runs"].append(new_run)
    
    response.headers["x-baxel-generation-source"] = "nvidia"
    response.headers["x-baxel-generation-model"] = "meta/llama-3.1-70b-instruct"
    response.headers["x-baxel-plan-code"] = "studio"
    
    return {
        "id": run_id,
        "status": "completed",
        "result": result
    }

@pipelines_router.get("/{pipeline_id}")
async def get_pipeline_status(pipeline_id: str, user=Depends(get_current_user)):
    uid = user.get("sub")
    run = next((r for r in in_memory_db["runs"] if r["id"] == pipeline_id and r["user_id"] == uid), None)
    if not run:
        raise HTTPException(status_code=404, detail="Pipeline run not found")
    return run

